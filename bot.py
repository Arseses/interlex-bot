def get_main_keyboard():
    keyboard = [
        [InlineKeyboardButton("📚 Описание программы", callback_data="description")],
        [InlineKeyboardButton("🌐 Изучаемые языки", callback_data="languages")],
        [InlineKeyboardButton("📖 Что изучают", callback_data="curriculum")],
        [InlineKeyboardButton("✅ Минимальные баллы", callback_data="scores")],
        [InlineKeyboardButton("🤝 Партнеры и работодатели", callback_data="partners")],
        [InlineKeyboardButton("📞 Контакты", callback_data="contacts")],
    ]
    return InlineKeyboardMarkup(keyboard)

async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user = update.effective_user
    logger.info(f"Пользователь {user.first_name} (id: {user.id}) запустил бота")
    await update.message.reply_text(
        TEXTS["welcome"],
        parse_mode="Markdown",
        reply_markup=get_main_keyboard()
    )

async def help_command(update: Update, context: ContextTypes.DEFAULT_TYPE):
    help_text = (
        "🆘 *Помощь*\n\n"
        "Я бот-помощник программы «Перевод и межкультурная коммуникация» УрФУ.\n\n"
        "📌 *Доступные команды:*\n"
        "/start - начать работу с ботом\n"
        "/help - показать это сообщение\n\n"
        "💡 *Совет:* Используйте кнопки для навигации!"
    )
    await update.message.reply_text(help_text, parse_mode="Markdown")

async def button_callback(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    callback_data = query.data
    logger.info(f"Пользователь нажал кнопку: {callback_data}")
    
    if callback_data in TEXTS:
        await query.edit_message_text(
            TEXTS[callback_data],
            parse_mode="Markdown",
            reply_markup=get_main_keyboard()
        )
    else:
        await query.edit_message_text(
            "❌ Извините, произошла ошибка. Попробуйте снова.",
            reply_markup=get_main_keyboard()
        )

async def error_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    logger.error(f"Ошибка: {context.error}")

def main():
    try:
        application = Application.builder().token(BOT_TOKEN).build()
        application.add_handler(CommandHandler("start", start))
        application.add_handler(CommandHandler("help", help_command))
        application.add_handler(CallbackQueryHandler(button_callback))
        application.add_error_handler(error_handler)
        
        logger.info("🚀 Бот запущен и готов к работе!")
        print("✅ Бот успешно запущен!")
        application.run_polling(allowed_updates=Update.ALL_TYPES)
        
    except Exception as e:
        logger.error(f"Ошибка при запуске бота: {e}")

if __name__ == "__main__":
    main()